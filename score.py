def vertex_sum(features, weights):
    value = 0
    for i in range(len(features['V'])):
        for j in range(len(features['V'][i])):
            value += weights['V'][j] * features['V'][i][j]

    return value


def edge_sum(features, weights):
    value = 0
    for i in range(0, len(features['E'])):
        for j in range(len(features['E'][i])):
            value += weights['E'][j] * features['E'][i][j]

    return value

def score(features, weights):
    return (1/len(features["V"]))*vertex_sum(features, weights) 
    + (1/len(features["E"]))*edge_sum(features, weights)

